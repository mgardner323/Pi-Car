$(function(){
	$('button').click(function(){
		$.ajax({
			url: '/x_increase',
			data: $('form').serialize(),
			type: 'POST',
			success: function(response){
				console.log(response);
			},
			error: function(error){
				console.log(error);
			}
		});
	});
});
